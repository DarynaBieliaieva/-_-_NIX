// camelCase

let userLoginCount = 0;
alert(userLoginCount);

let userLoginCount = 0;
console.log(userLoginCount);

// PascalCase

let UserLoginCount = 1;
alert(UserLoginCount);

let UserLoginCount = 1;
console.log(UserLoginCount);

// snake_case

let user_login_count = 2;
alert(user_login_count);


let user_login_count = 2;
console.log(user_login_count);

/*
В JavaScript функция alert позволяет вывести какой-либо текст в окно браузера в виде диалогового окна.

Метод console.log() — это способ вывести отладочную информацию, не мешая пользователю.
*/